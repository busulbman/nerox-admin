export function t(lang: MenuLanguage, key: string, params?: Record<string, string | number>): string {
  const value = translations[lang]?.[key] ?? translations[DEFAULT_LANGUAGE]?.[key] ?? key

  if (!params) return value

  return Object.entries(params).reduce(
    (result, [paramKey, paramValue]) => result.replace(`{${paramKey}}`, String(paramValue)),
    value
  )
}

export function getLanguageStorageKey(tableId: string) {
  return `nerox_lang_${tableId}`
}

export function getCustomerIdKey() {
  return 'nerox_customer_id'
}

export function getOrCreateCustomerId(): string {
  if (typeof window === 'undefined') return ''

  const existing = window.localStorage.getItem(getCustomerIdKey())
  if (existing) return existing

  const newId = globalThis.crypto?.randomUUID?.() ?? `cust-${Math.random().toString(16).slice(2)}-${Date.now().toString(36)}`
  window.localStorage.setItem(getCustomerIdKey(), newId)
  return newId
}

export function readStoredLanguage(tableId: string): MenuLanguage | null {
  if (typeof window === 'undefined') return null

  const stored = window.localStorage.getItem(getLanguageStorageKey(tableId))
  if (stored && SUPPORTED_LANGUAGES.some((lang) => lang.code === stored)) {
    return stored as MenuLanguage
  }
  return null
}

export function saveLanguage(tableId: string, lang: MenuLanguage) {
  if (typeof window === 'undefined') return
  window.localStorage.setItem(getLanguageStorageKey(tableId), lang)
}

export function getDefaultCustomerName(lang: MenuLanguage): string {
  return t(lang, 'defaultCustomerName')
}
