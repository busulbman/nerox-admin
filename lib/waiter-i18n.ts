export function tw(lang: WaiterLanguage, key: string, params?: Record<string, string | number>): string {
  const value = translations[lang]?.[key] ?? translations[DEFAULT_WAITER_LANGUAGE]?.[key] ?? key

  if (!params) return value

  return Object.entries(params).reduce(
    (result, [paramKey, paramValue]) => result.replace(`{${paramKey}}`, String(paramValue)),
    value
  )
}

export function readStoredWaiterLanguage(): WaiterLanguage | null {
  if (typeof window === 'undefined') return null
  const stored = window.localStorage.getItem(WAITER_LANGUAGE_KEY)
  if (stored && WAITER_LANGUAGES.some((lang) => lang.code === stored)) {
    return stored as WaiterLanguage
  }
  return null
}

export function saveWaiterLanguage(lang: WaiterLanguage) {
  if (typeof window === 'undefined') return
  window.localStorage.setItem(WAITER_LANGUAGE_KEY, lang)
}

export function getWaiterTableStatusLabel(lang: WaiterLanguage, status: string): string {
  const key: Record<string, string> = {
    boş: 'statusEmpty',
    aktif: 'statusActive',
    'çağrı var': 'statusCall',
    'hesap istendi': 'statusBill',
    temizlik: 'statusCleaning',
    kapalı: 'statusClosed',
  }
  return key[status] ? tw(lang, key[status]) : status
}
